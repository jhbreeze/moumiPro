package com.moumi.app.admin.comment;

public class Comment {
	private long stopNum;
	private String content;
	private String userName;
	private int status;
	private long userCode;
	private String subject;
	private String brandName;
	private long communityNum;
	private String sendName;
	private String writeName;
	private String notifyContent;
	private String regDate;
	private String bigBrandName;
	private String smallBrandName;
	
	public long getStopNum() {
		return stopNum;
	}
	public void setStopNum(long stopNum) {
		this.stopNum = stopNum;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	public long getUserCode() {
		return userCode;
	}
	public void setUserCode(long userCode) {
		this.userCode = userCode;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getBrandName() {
		return brandName;
	}
	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}
	public long getCommunityNum() {
		return communityNum;
	}
	public void setCommunityNum(long communityNum) {
		this.communityNum = communityNum;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getSendName() {
		return sendName;
	}
	public void setSendName(String sendName) {
		this.sendName = sendName;
	}
	public String getWriteName() {
		return writeName;
	}
	public void setWriteName(String writeName) {
		this.writeName = writeName;
	}
	public String getNotifyContent() {
		return notifyContent;
	}
	public void setNotifyContent(String notifyContent) {
		this.notifyContent = notifyContent;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public String getBigBrandName() {
		return bigBrandName;
	}
	public void setBigBrandName(String bigBrandName) {
		this.bigBrandName = bigBrandName;
	}
	public String getSmallBrandName() {
		return smallBrandName;
	}
	public void setSmallBrandName(String smallBrandName) {
		this.smallBrandName = smallBrandName;
	}
	
	

}
